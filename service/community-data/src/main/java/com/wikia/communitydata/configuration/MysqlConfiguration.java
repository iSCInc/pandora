package com.wikia.communitydata.configuration;

public class MysqlConfiguration {
  private String db;
  private String host;
  private int port;
  private String user;
  private String password;

  public String getDb() {
    return db;
  }

  public String getHost() {
    return host;
  }

  public int getPort() {
    return port;
  }

  public String getUser() {
    return user;
  }

  public String getPassword() {
    return password;
  }
}
